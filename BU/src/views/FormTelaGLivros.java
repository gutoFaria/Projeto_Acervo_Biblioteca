    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        boolean confere = ConfereTextField();
        
        if(confere){
            Livro l = new Livro();
            l.setIdRegistro(Integer.parseInt(txtIdRegistro.getText()));
            l.setTitulo(txtTitulo.getText());
            l.setAssunto(txtAssunto.getText());
            l.setAutores(txtAutor.getText());
            l.setAno(txtAno.getText());
            l.setEditora(txtEditora.getText());
            l.setNumEdicao(Integer.parseInt(txtEdicao.getText()));
            l.setCdu(txtCdu.getText());
            l.setCidade(txtCidade.getText());
            l.setNumPaginas(Integer.parseInt(txtNumpaginas.getText()));
            
            LivroController lc = new LivroController();
            
            lc.AtualizarLivro(l);
            PreencherTabela();
        }else{
            JOptionPane.showMessageDialog(null,"Preencha todos os campos!");
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

