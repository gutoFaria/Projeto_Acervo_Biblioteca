    public void PreencherTabela(){
        LivroController lc = new LivroController();
        List<Livro> lista = lc.ListarLivros();
        
        DefaultTableModel  modeloTabela = (DefaultTableModel) tabelaLivro.getModel();
        modeloTabela.setRowCount(0);
        
        for(Livro l : lista){
            modeloTabela.addRow(new Object[]{
                l.getIdRegistro(),
                l.getTitulo(),
                l.getAssunto(),
                l.getAutores(),
                l.getAno(),
                l.getNumPaginas(),
                l.getEditora(),
                l.getNumEdicao(),
                l.getCdu(),
                l.getCidade()
            });
        }
    } 
